package MyAlgorithm;

// 题目描述
// 请实现一个函数，将一个字符串中的每个空格替换成“%20”。例如，当字符串为We Are Happy.则经过替换之后的字符串为We%20Are%20Happy
public class Replace_space {
	public static void main(String args[]){
		StringBuffer str = new StringBuffer("");
		str.append("We are the world");
		for(int i=0; i<str.length(); i++){ //长度表示用length()
			char c = str.charAt(i); // 找到StringBuffer上的字符
			if(c == ' '){
				str.replace(i, i+1, "%20"); // 替换，注意开头和结尾的标号
			}
		}
		System.out.println(str.substring(0, str.length())); // 将StringBuffer表示为String类型
	}
}
